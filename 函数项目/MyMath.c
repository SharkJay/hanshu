//将函数单独做成一个文件
int sum_math(int a, int b) {
	return a + b;
}

int sub_math(int a, int b) {
	return a - b;

}

int mul_math(int a, int b) {
	return a * b;
}

int div_math(int a, int b) {
	return a / b;
}

int max_math(int a, int b) {
	return a > b ? a : b;
}

int min_math(int a, int b) {
	return a < b ? a : b;
}